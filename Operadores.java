public class Operadores {
    public static void main(String[] args) {
        //operadores aritiméticos
        System.out.println(1 + 2);
        System.out.println(1 - 2);
        System.out.println(2 / 2);
        System.out.println(1 * 2);
        System.out.println(2 % 2);
        System.out.println(2 ^ 2);
        // operadores de comparações
        System.out.println(1 > 1);
        System.out.println(1 < 1);
        System.out.println(1 >= 1);
        System.out.println(1 <= 1);
        System.out.println(1 != 1);
        System.out.println(1 == 1);
        //operadores E e OU
        // E -> &&
        // OU -> ||
    }
}
